<template>
  <kui-popover
    v-bind="$attrs"
    v-model="visible"
    trigger="click"
  >
  <div class="kui-popconfirm">
    <p class="kui-popconfirm__main">
    <i
      v-if="!hideIcon"
      :class="icon"
      class="kui-popconfirm__icon"
      :style="{color: iconColor}"
    ></i>
      {{title}}
    </p>
    <div class="kui-popconfirm__action">
      <kui-button
        size="mini"
        :type="cancelButtonType"
        @click="cancel"
      >
        {{ displayCancelButtonText }}
      </kui-button>
      <kui-button
        size="mini"
        :type="confirmButtonType"
        @click="confirm"
      >
        {{ displayConfirmButtonText }}
      </kui-button>
    </div>
  </div>
  <slot name="reference" slot="reference"></slot>
</kui-popover>
</template>

<script>
import KuiPopover from 'karman-ui/packages/popover';
import KuiButton from 'karman-ui/packages/button';
import {t} from 'karman-ui/src/locale';

export default {
  name: 'KuiPopconfirm',
  props: {
    title: {
      type: String
    },
    confirmButtonText: {
      type: String
    },
    cancelButtonText: {
      type: String
    },
    confirmButtonType: {
      type: String,
      default: 'primary'
    },
    cancelButtonType: {
      type: String,
      default: 'text'
    },
    icon: {
      type: String,
      default: 'kui-icon-question'
    },
    iconColor: {
      type: String,
      default: '#f90'
    },
    hideIcon: {
      type: Boolean,
      default: false
    }
  },
  components: {
    KuiPopover,
    KuiButton
  },
  data() {
    return {
      visible: false
    };
  },
  computed: {
    displayConfirmButtonText() {
      return this.confirmButtonText || t('lang.popconfirm.confirmButtonText');
    },
    displayCancelButtonText() {
      return this.cancelButtonText || t('lang.popconfirm.cancelButtonText');
    }
  },
  methods: {
    confirm() {
      this.visible = false;
      this.$emit('confirm');
    },
    cancel() {
      this.visible = false;
      this.$emit('cancel');
    }
  }
};
</script>
